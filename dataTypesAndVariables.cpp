#include <iostream>
using namespace std;

int main(){
	int a; //deklarasi variabel
	a = 10; //inisialisasi variabel
	
	int x = 5; //deklarasi variabel bisa dilakukan dengan seperti ini
	cout << x << endl;
	
	float f = 3.14; //deklarasi variabel float
	cout << f << endl;
	
	char c = 'I'; //deklarasi karakter menggunakan kutip 1
	cout << c << endl;
	
	string str = "ITHB"; //deklarasi karakter menggunakan kutip 2
	cout << str;
	
	bool b = true; //deklarasi boolean
	cout << b; //1 true 0 false
}
